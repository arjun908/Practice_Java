public class OperatorDemo {

    public static void main(String[] args) {

        int m=6;
        int n=2;
        int add=m+n;
        int sub=m-n;
        int pro=m*n;
        double div=m/n;
        int rem=m%n;

        System.out.println(add);
        System.out.println(sub);
        System.out.println(pro);
        System.out.println(div);
        System.out.println(rem);

    }
}
